

"use strict";

    // Сложение
    /**
     * @param {number} a
     * @param {number} d
     * @returns {number}
     */
function result1(a, d) {
    return a + d;
}

    // Вычитание
    /**
     * @param {number} a
     * @param {number} d
     * @returns {number}
     */
function result2(a, d) {
    return a - d;
}

    // Деление
    /**
     * @param {number} a
     * @param {number} d
     * @returns {number}
     */
function result3(a, d) {
    return a / d;
}

    // Умножение
    /**
     * @param {number} a
     * @param {number} d
     * @returns {number}
     */
function result4(a, d) {
    return a * d;
}